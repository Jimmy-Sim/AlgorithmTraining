while True:
    abbr = input()

    if abbr == "CU":
        print("see you")
    elif abbr == ":-)":
        print("I’m happy")
    elif abbr == ":-(":
        print("I’m unhappy")
    elif abbr == ";-)":
        print("wink")
    elif abbr == ":-P":
        print("stick out my tongue")
    elif abbr == "(~.~)":
        print("sleepy")
    elif abbr == "TA":
        print("totally awesome")
    elif abbr == "CCC":
        print("Canadian Computing Competition")
    elif abbr == "CUZ":
        print("because")
    elif abbr == "TY":
        print("thank-you")
    elif abbr == "YW":
        print("you’re welcome")
    elif abbr == "TTYL":
        print("talk to you later")
        break
    else:
        print(abbr)